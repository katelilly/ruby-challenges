weather_today = 'sunny'
case weather_today
when 'sunny'
puts "Wear your finest sunscreen, favorite silk maxi skirt and don't forget your sunglasses!"
when 'rainy'
puts "A rainjacket with a hood is great at keeping the rain off your head."
when 'foggy'
puts "Find your softest hoodie to get snuggy, whether you're inside or out."
when 'snowy'
puts "Don't forget to wear lots of layers, think wool sweaters, and a nice scarf."
end
